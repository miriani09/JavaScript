function day(){
    d = Math.floor(Math.random()*7)
    if(d == 0){
        document.write("Sunday")
    }
    else if(d == 1){
        document.write("Monday")
    }
    else if(d == 2){
        document.write("Tuesday")
    }
    else if(d == 3){
        document.write("Wednasday")
    }
    else if(d == 4){
        document.write("Thursday")
    }
    else if(d == 5){
        document.write("Friday")
    }
    else if(d == 6){
        document.write("Sutarday")
    }
}

